package com.syntax.class02;

public class WaysToCreateVariable {
	
	public static void main(String[] args) {
		
		//to create a variable we need two things: Data Type & Name
		
		byte temperature=60;//anything before the variable is called a declaration or creation of the variable
		
		/*
		 * 2 ways that this is done in 2 steps
		 * create a variable (Data Type and Name)
		 * assign the value
		 */
		
		int sum;//create variable
		sum=30;//assign the value
		sum=50;//reassigning the value
		
		int num1, num2, num3;//create 3 variables of int type
		num1=10;//assign the value
		num2=20;//assign the value
		num3=30;//assign the value
		System.out.println(sum);
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		
	}

}
