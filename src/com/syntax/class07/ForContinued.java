package com.syntax.class07;

public class ForContinued {
public static void main(String[] args) {
	
	for(int i=1; i<=90; i++) {
		System.out.println(i+" ");}
		
		System.out.println("-----------");
		
		for(int i=60; i>=10; i--) {
			System.out.print(i+" ");
		}
		System.out.println("------------");
		
		
	
}
}
